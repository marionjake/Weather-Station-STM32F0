  log_print(INFO_L, "STARTING OS TASKS !\n");

  if(xTaskCreate(temperature_task, "temperature task", 256, NULL, 1, &temperature_handle) != pdPASS)
    log_print(ERROR_L, "Failed to create temperature task !\n");
  if(xTaskCreate(lcd_task, "lcd task", 128, NULL, 1, &lcd_handle) != pdPASS)
    log_print(ERROR_L, "Failed to create lcd task!\n");
  
  
